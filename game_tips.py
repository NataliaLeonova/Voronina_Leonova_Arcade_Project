import random


class GameTips:
    """Случайные подсказки в игре"""

    TIPS = [
        "Подсказка: Игра анализирует ваши реакции...",
        "Совет: Держите карту открытой для навигации",
        "Внимание: Монстры реагируют на звук",
        "Совет: Фонарик разряжается в темноте",
        "Подсказка: Крики отпугивают монстров",
        "Совет: Темнота увеличивает стресс",
        "Внимание: Игра становится сложнее со временем",
        "Подсказка: Ищите ключи в тупиках"
    ]

    @staticmethod
    def get_random_tip():
        """Получить случайную подсказку"""
        return random.choice(GameTips.TIPS)

    @staticmethod
    def get_context_tip(context):
        """Получить контекстную подсказку"""
        context_tips = {
            'lost': "Вы потерялись? Используйте карту (M)",
            'low_health': "Мало здоровья? Избегайте монстров!",
            'high_stress': "Высокий стресс? Найдите светлое место",
            'no_keys': "Нет ключей? Ищите в боковых проходах",
            'dark': "Темно? Включите фонарик (F)",
            'monster_near': "Монстр рядом? Крикните (ПРОБЕЛ)"
        }
        return context_tips.get(context, GameTips.get_random_tip())